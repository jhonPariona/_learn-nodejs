/*----------------------------------------------------------------------------/*
  $Load library http
/*----------------------------------------------------------------------------*/
const http = require('http')


/*----------------------------------------------------------------------------/*
  $Define Server
/*----------------------------------------------------------------------------*/
const server = http.createServer(function (req, res) {
  // response
  res.writeHead(200, {'Content-type':'text/html'});

  res.end('Hello, world from Http server <b>Wilder</b>');
})

/*----------------------------------------------------------------------------/*
  $Run server
/*----------------------------------------------------------------------------*/

server.listen(1337,'127.0.0.1');
